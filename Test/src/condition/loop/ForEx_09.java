package condition.loop;

public class ForEx_09 {

	public static void main(String[] args) {
		
		// 중첩 for 문
		
		// 구구단 출력
		
		for(int dan=2 ; dan <=9 ; dan++) {
			
			// 8회, 2~9 단
			
			for(int i=1; i<=9; i++) {
				
				// 9회, 1~9
				
				// 총 72회
				
				System.out.println(dan + " x " + i + "=" + (dan*i));
				
			}
			
			System.out.println("--------------------------");
				
			// 총 8회	
				
			}
			
			
			
		}
		
		
		
		

		
				
	}
	

package condition.star;

public class Star_13 {

	public static void main(String[] args) {
		
	for(int n=0 ; n <5 ; n++) {
			
			for(int m=0 ; m<5-n ; m++) {
							
				System.out.print("*");				
			
			}
			System.out.println();
		}
		
		
	for(int n=0 ; n <5 ; n++) {
		
		for(int m=0 ; m<n+1; m++) {
	
			System.out.print("*");		
	
         }
		
		System.out.println();
	 }
	
	
}}




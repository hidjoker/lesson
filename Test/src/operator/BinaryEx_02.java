		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		System.out.println(num1 < num2);
		System.out.println(num1 > num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 >= num2);
		
		
		System.out.println("----예외----");
		
		//boolean
		System.out.println(true == true);
		// System.out.println(true < true); //boolean은 숫자로 취급되지 않는다. 크기 비교가 되지 않는다.
